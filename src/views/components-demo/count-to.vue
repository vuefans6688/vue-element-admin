<template>
  <div class="components-container">
    <aside>
      <a
        href="https://github.com/PanJiaChen/vue-countTo"
        target="_blank"
      >计算总数组件</a>
    </aside>
    <count-to
      ref="example"
      :start-val="_startVal"
      :end-val="_endVal"
      :duration="_duration"
      :decimals="_decimals"
      :separator="_separator"
      :prefix="_prefix"
      :suffix="_suffix"
      :autoplay="false"
      class="example"
    />
    <div style="margin-left: 25%; margin-top: 40px">
      <label
        class="label"
        for="startValInput"
      >起始值:
        <input
          v-model.number="setStartVal"
          type="number"
          name="startValInput"
        >
      </label>
      <label
        class="label"
        for="endValInput"
      >结束值:
        <input v-model.number="setEndVal" type="number" name="endVaInput">
      </label>
      <label
        class="label"
        for="durationInput"
      >期间:
        <input
          v-model.number="setDuration"
          type="number"
          name="durationInput"
        >
      </label>
      <div class="start-button example-button" @click="start">开始</div>
      <div class="pause-resume-button example-button" @click="pauseResume">
        暂停/继续
      </div>
      <br>
      <label
        class="label"
        for="decimalsInput"
      >小数:
        <input
          v-model.number="setDecimals"
          type="number"
          name="decimalsInput"
        >
      </label>
      <label
        class="label"
        for="separatorInput"
      >分离器:
        <input v-model="setSeparator" name="separatorInput">
      </label>
      <label
        class="label"
        for="prefixInput"
      >前缀:
        <input v-model="setPrefix" name="prefixInput">
      </label>
      <label
        class="label"
        for="suffixInput"
      >后缀:
        <input v-model="setSuffix" name="suffixInput">
      </label>
    </div>
    <aside>
      &lt;count-to :start-val=&#x27;{{ _startVal }}&#x27; :end-val=&#x27;{{
        _endVal
      }}&#x27; :duration=&#x27;{{ _duration }}&#x27; :decimals=&#x27;{{
        _decimals
      }}&#x27; :separator=&#x27;{{ _separator }}&#x27; :prefix=&#x27;{{
        _prefix
      }}&#x27; :suffix=&#x27;{{ _suffix }}&#x27; :autoplay=false&gt;
    </aside>
  </div>
</template>

<script>
import countTo from 'vue-count-to'
export default {
  name: 'CountToDemo',
  components: { countTo },
  data() {
    return {
      setStartVal: 0,
      setEndVal: 2017,
      setDuration: 4000,
      setDecimals: 0,
      setSeparator: ',',
      setSuffix: ' rmb',
      setPrefix: '¥ '
    }
  },
  computed: {
    _startVal() {
      if (this.setStartVal) {
        return this.setStartVal
      } else {
        return 0
      }
    },
    _endVal() {
      if (this.setEndVal) {
        return this.setEndVal
      } else {
        return 0
      }
    },
    _duration() {
      if (this.setDuration) {
        return this.setDuration
      } else {
        return 100
      }
    },
    _decimals() {
      if (this.setDecimals) {
        if (this.setDecimals < 0 || this.setDecimals > 20) {
          alert('数字参数必须介于0和20之间')
          return 0
        }
        return this.setDecimals
      } else {
        return 0
      }
    },
    _separator() {
      return this.setSeparator
    },
    _suffix() {
      return this.setSuffix
    },
    _prefix() {
      return this.setPrefix
    }
  },
  methods: {
    start() {
      this.$refs.example.start()
    },
    pauseResume() {
      this.$refs.example.pauseResume()
    }
  }
}
</script>

